#type casting means a data type is converted into another data type by programmer using casting operator
#type conversion - a data type is automatically converted into another data type by compiler at the compiler time 

# Convert integer to float
age = 25
age_as_float = float(age)
print("Age as float:", age_as_float) 

# Convert float to integer
price = 19.99
price_as_int = int(price)
print("Price as integer:", price_as_int) 

# Convert integer to string
quantity = 100
quantity_as_str = str(quantity)
print("Quantity as string:", quantity_as_str)  # Output: "100"

# Convert string to integer
user_input = "42"
user_input_as_int = int(user_input)
print("User input as integer:", user_input_as_int) 

